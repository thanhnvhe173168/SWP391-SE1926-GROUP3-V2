package model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Stock {

    private int id, laptopId, userId, quantity;
    private Date date;
    private String action;

    public Stock() {
    }

    public Stock(int laptopId, int userId, int quantity, String action) {
        this.laptopId = laptopId;
        this.userId = userId;
        this.quantity = quantity;
        this.action = action;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLaptopId() {
        return laptopId;
    }

    public void setLaptopId(int laptopId) {
        this.laptopId = laptopId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

}
