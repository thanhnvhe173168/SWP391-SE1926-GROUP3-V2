package model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Promotion {

    private int id;
    private String image, title, status;
    private Date startDate, endDate;

    public Promotion() {
    }

    public Promotion(String image, String title, Date startDate, Date endDate) {
        this.image = image;
        this.title = title;
        this.startDate = startDate;
        this.endDate = endDate;
    }

    public Promotion(int id, String image, String title, Date startDate, Date endDate) {
        this.id = id;
        this.image = image;
        this.title = title;
        this.startDate = startDate;
        this.endDate = endDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

}
